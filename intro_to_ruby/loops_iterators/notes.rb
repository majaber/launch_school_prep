Loops & Iterators

while loops - given a parameter that evaluates to a boolean and will run until
  that expression returns false .

do/while loops work in a similar fashion, however it will run the loop once no matter what.
the conditional comes in at the end.

FOR loops have a definitive end and will not cause infinite loops.
Loops over a collection of elements.

Iterators are methods that naturally loop over a given set
of date and allow you to operate on each element of the collection.
